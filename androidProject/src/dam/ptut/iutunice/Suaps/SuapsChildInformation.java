package dam.ptut.iutunice.Suaps;

public class SuapsChildInformation {
	String Name;
	String Email;

	public SuapsChildInformation(String name, String email) {
		this.Name = name;
		this.Email = email;

	}

	public String getName() {
		return Name;
	}

	public String getEmail() {
		return Email;
	}
}
