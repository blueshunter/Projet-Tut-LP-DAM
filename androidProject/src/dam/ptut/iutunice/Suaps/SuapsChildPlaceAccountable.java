package dam.ptut.iutunice.Suaps;

public class SuapsChildPlaceAccountable {
	String Name;
	String Email;

	public SuapsChildPlaceAccountable(String name, String email) {
		this.Name = name;
		this.Email = email;
	}

	public String getName() {
		return Name;
	}

	public String getEmail() {
		return Email;
	}
}
